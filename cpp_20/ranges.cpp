#include <cmdline.h> 
#include "simplelog/simplelog.h"

#include <iterator>
#include <ranges>
#include <vector>

int main(int argc, char *argv[])
{
  cmdline::parser arg;
  arg.add("help", 'h', "Print help.");
//  arg.add<std::string>("file", 'f', "Example file argument.", true);
    
  if(!arg.parse(argc, const_cast<const char* const*>(argv)))
  {
    const auto fullErr = arg.error_full();
    if(!fullErr.empty())
      log << fullErr;
     
    log << arg.usage();
    return 0;
  }
  
  if(arg.exist("help"))
  {
    log << arg.usage();
    return 0;
  } 

  /* Example file arg check and get
  if(!arg.exist("file"))
  {
    log << "--file or -f argument is mandatory!";
    log << arg.usage();
    return 0;
  }
  
  const std::string file = arg.get<std::string>("file");
  */



  std::vector<int> integers { 11, 2, 5, 4};
  log << "Not sorted: " << integers; 
  std::ranges::sort(integers);
  log << "Sorted: " << integers; 

  std::ranges::reverse(integers);
  log << "Reversed: " << integers;

 
  return 0;
}
